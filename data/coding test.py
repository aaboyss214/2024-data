# 4로나누면 1 = 1
# 4로나누면 2 = 2
# 4로나누면 3 = 3
# 4로나누면 0 = 4
# 위에건 행
# 아래는 열
# 4로나누면 몫이 0
# 4번재행만 몫이 나머지에 비해 +1
a, b = map(int, input().split())

a_ = [a//4, a%4]
b_ = [b//4, b%4]
if a % 4 == 0:
    a_[0] -= 1
    a_[1] += 4
if b % 4 == 0:
    b_[0] -= 1
    b_[1] += 4
print(abs(a_[0]-b_[0])+abs(a_[1]-b_[1]))

